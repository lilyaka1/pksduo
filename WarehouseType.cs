using System;

namespace WarehouseManagementSystem
{
	/// <summary>
	/// Перечисление типов складов в системе
	/// </summary>
	public enum WarehouseType
	{
		/// Холодный склад для скоропортящихся товаров (срок < 30 дней)
		Cold,

		/// Сортировочный склад для товаров со смешанными сроками хранения
		Sorting,


		/// Общий склад для товаров длительного хранения (срок >= 30 дней)
		General,

		/// Склад утилизации для просроченных товаров
		Disposal
	}
}
